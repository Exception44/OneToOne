package com.prac.buisnessbean;

public class AssetBean {
private Integer assetId;
private String assetName;
private String assetBrandName;
private Integer assetValidityYear;
public Integer getAssetId() {
	return assetId;
}
public void setAssetId(Integer assetId) {
	this.assetId = assetId;
}
public String getAssetName() {
	return assetName;
}
public void setAssetName(String assetName) {
	this.assetName = assetName;
}
public String getAssetBrandName() {
	return assetBrandName;
}
public void setAssetBrandName(String assetBrandName) {
	this.assetBrandName = assetBrandName;
}
public Integer getAssetValidityYear() {
	return assetValidityYear;
}
public void setAssetValidityYear(Integer assetValidityYear) {
	this.assetValidityYear = assetValidityYear;
}

}
